    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (sistema.getSorteo() == null) {
            JOptionPane.showMessageDialog(null, "No hay sorteo definido", "Error", 0);
        }
        if (sistema.getSorteo().getListaParticipantes().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay particiantes para el sorteo", "Éxito", 1);
        } else {
            try {
                sistema.realizarSorteo();
            } catch (MessagingException ex) {
                Logger.getLogger(VentanaRestaurante.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Se ha realizado el sorteo, se contactará a el/los ganador/es", "Éxito", 1);
            sistema.setListaClientes(new ArrayList());
            sistema.getSorteo().setListaParticipantes(new ArrayList());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

