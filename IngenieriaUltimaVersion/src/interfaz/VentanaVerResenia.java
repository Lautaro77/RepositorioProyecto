    private void llenarLista() {
        String texto = "";
        for (int i = 0; i < sistema.getListaEvaluaciones().size(); i++) {
            texto += sistema.getListaEvaluaciones().get(i) + "\n";
        }
        if (texto.equals("")) {
            texto = "No se han realizado reseñas hasta el momento";
        }
        jTextArea1.setText(texto);
        jTextArea1.setEditable(false);
    }

